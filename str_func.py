def get_title_str(string):
    """
    Функция, которая возвращает строку с первыми зглавными буквами
    """
    title_string = string.title()

    return title_string


def get_upper_str(string):
    """
    Функция, которая возвращает строку со всеми заглавными буквами
    """
    upper_string = string.upper()

    return upper_string
